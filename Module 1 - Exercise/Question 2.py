# Write a code for add the following single element and multiple elements to the existing set A={2,3,4,5}:
# (i) 6, (ii) 7,8,9 and 10.

# Existing set A
A = {2, 3, 4, 5}

# Add a single element (6) to set A
A.add(6)

# Add multiple elements (7, 8, 9, 10) to set A
A.update({7, 8, 9, 10})

# Print the updated set A
print("Updated set A:", A)
